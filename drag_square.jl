# ╔═╡ 1902bcab-af06-4355-b8e6-fbebf30014c9
md"""
### 🌊 Physical Parameters and Geometry

We define the basic physical parameters:

- `U∞`: inflow velocity magnitude.
- `D`: diameter of the bluff body (used for computing Reynolds number and drag).
- `H`: height of the domain (equivalent to the flume width).

These define the flow conditions and set the scale for non-dimensional numbers.
"""

# ╔═╡ cbd21e41-0637-4c63-9e24-e56747bacdb9
begin
	U∞ = 0.281
	D = 0.04
	H = 0.1795
	nothing # to avoid output message
end

# ╔═╡ 8fe68b13-b705-46bd-aa19-d3b23340c456
md"""
### 🗺️ Mesh and Domains

We load a 2D mesh (in `.msh` format) created using Gmsh. The domain consists of:

- `Ω`: the fluid domain (interior).
- `Γin`: the inlet boundary.
- `Γout`: the outlet boundary.
- `Γside`, `Γwall`: side walls and obstacle boundaries.

We associate these physical boundaries using their Gmsh-defined tags.
"""

# ╔═╡ 221e4c5d-4b32-47d8-a923-9e9a1deeab89
begin
    model = GmshDiscreteModel("meshes/square.msh")
    Ω = Interior(model)
    Γout = Boundary(model,tags="outflow")
    Γin = Boundary(model,tags="inlet")
    Γside = Boundary(model, tags="sides")
    Γwall = Boundary(model, tags="walls")
	nothing # to avoid output message
end

# ╔═╡ 3e888b41-5225-489f-85d2-cceb1a90fffc
md"""
### 📥 Boundary Conditions

We define:

- `uin`: a function prescribing constant inflow velocity (Dirichlet condition at inlet).
- `uwall`: zero velocity for no-slip condition on walls and the obstacle.

These are passed as functions of time for transient simulation.
"""

# ╔═╡ 0b9c530c-6f84-4a04-a923-c9ab10a33166
begin
	uin(x,t) = VectorValue(U∞,0.0)
	uin(t::Real) = x->uin(x,t)
	uwall(x,t) = VectorValue(0.0,0.0)
	uwall(t::Real) = x -> uwall(x,t)
	nothing # to avoid output message
end

# ╔═╡ f07a74b9-fbf7-4726-a853-d6a71ff25ddf
md"""
### 📊 Viscosity and Reynolds Number

- `ν`: kinematic viscosity of the fluid (e.g., water).
- `Re`: Reynolds number, a dimensionless number indicating the flow regime.

$$Re = \frac{U_\infty D}{\nu}$$

It determines whether the flow is laminar or turbulent.
"""

# ╔═╡ f3a87466-3fc2-44f7-afb5-d34fbba934ea
begin
	ν = 1.0e-6
	Re = U∞*D/ν
end

# ╔═╡ d2055522-0055-47b6-b22d-9fdd8fdb2b4e
md"""
### 🧱 Finite Element Spaces

We use **Taylor-Hood elements**:

- Velocity reference element `reffeᵤ`: Quadratic (`P2`) elements
- Pressure reference element `reffeₚ`: Linear (`P1`) elements.

This choice satisfies the **inf-sup** (LBB) condition necessary for incompressible flows.
"""

# ╔═╡ 2e5eac60-7b32-4642-87f1-62ffe28fadec
begin
	order = 2
	reffeᵤ = ReferenceFE(lagrangian,VectorValue{2,Float64},order)
	reffeₚ = ReferenceFE(lagrangian,Float64,order-1)
	nothing # to avoid output message
end

# ╔═╡ 4b2f3b15-4689-46ad-985d-2196189c3bf4
md"""
### 🧮 Function Spaces

We build:

- `V`: velocity test space with Dirichlet conditions on inlet, side walls, and obstacle.
- `Q`: pressure test space with no essential BCs.
- `U`, `P`: trial spaces (time-dependent for velocity).

Using `dirichlet_masks` argument we can select which components of the velocity are constrained: all components for inlet and obstacle walls, and only vertical component on the sides.

Finally, we combine these into a mixed space `X × Y` to solve the Navier-Stokes system.
"""

# ╔═╡ 696f29d4-597e-4529-9be4-09573ecac425
begin
	V = TestFESpace(Ω,reffeᵤ,
				 	dirichlet_tags=["inlet","sides","walls"],
				 	dirichlet_masks=[(true,true),(false,true),(true,true)])
	Q = TestFESpace(Ω,reffeₚ)
	U = TransientTrialFESpace(V,[uin,uwall,uwall])
	P = TrialFESpace(Q)
	Y = MultiFieldFESpace([V,Q])
	X = TransientMultiFieldFESpace([U,P])
	nothing # to avoid output message
end

# ╔═╡ 4d87d987-f436-4859-bba2-bd22174ab96e
md"""
### 📏 Integration Setup

We define:

- `dΩ`: measure for integrating over the fluid domain.
- `dΓout`, `dΓwall`: measures for integrating over boundaries (outlet, wall).
- `nΓwall`: normal vector at wall boundaries (needed for stress computation).

This allows us to integrate the weak forms of the problem.
"""

# ╔═╡ 1c8ab6d3-adb6-42a0-bc31-0d0798bf758c
begin
	degree = 2*order
	dΩ = Measure(Ω,degree)
	dΓout = Measure(Γout,degree)
	nΓout = get_normal_vector(Γout)
	dΓwall = Measure(Γwall,degree)
	nΓwall = get_normal_vector(Γwall)
	nothing # to avoid output message
end

# ╔═╡ 491e3b88-138c-4d9d-8604-61050fd9ea00
md"""
### 🧩 Weak Form of the Stabilized Navier-Stokes Equations

We consider the **incompressible Navier-Stokes equations** in weak form with SUPG (Streamline-Upwind Petrov-Galerkin) stabilization. Let $\mathbf{V}$ and $Q$ be the function spaces for velocity and pressure. We seek $(\mathbf{u}, p) \in \mathbf{V} \times Q$ such that for all $(\mathbf{v}, q) \in \mathbf{V}_0 \times Q$:

$$\begin{aligned}
&\int_\Omega \left[ \frac{\partial \mathbf{u}}{\partial t} \cdot \mathbf{v} + ((\mathbf{u} \cdot \nabla)\mathbf{u}) \cdot \mathbf{v} + 2\nu \boldsymbol{\varepsilon}(\mathbf{u}) : \boldsymbol{\varepsilon}(\mathbf{v}) - p \, \nabla \cdot \mathbf{v} + q \, \nabla \cdot \mathbf{u} \right] \, d\Omega \\
&\quad + \sum_K \int_{\Omega_K} \tau_K \mathcal{L}_u(v)  \cdot \mathcal{R}_u(u) \, d\Omega_K
= \int_\Omega \mathbf{f} \cdot \mathbf{v} \, d\Omega,
\end{aligned}$$

where the residual of the strong form of the momentum equation is:

$$\mathcal{R}_u(u) = \frac{\partial \mathbf{u}}{\partial t} + (\mathbf{u} \cdot \nabla)\mathbf{u} + \nabla p - \nu \Delta \mathbf{u}.$$

and the linearized advection operator is:

$$\mathcal{L}_u(v) = (\mathbf{u} \cdot \nabla \mathbf{v})$$

The stabilization parameter $\tau_K$ depends on the local mesh size, velocity magnitude, and viscosity, and can be tuned for accuracy and stability.

---

### ⚠️ Why Stabilization?

The stabilization term improves numerical robustness by:
- **Suppressing spurious oscillations** in convection-dominated flows. It can also be understood as a turbulent model that dissipates energy at appropriate scales.
- **Allowing equal-order interpolation** for velocity and pressure (e.g., P1–P1 elements)

"""

# ╔═╡ b6ba6662-ef23-4b8c-b07f-68769ac4dd56
begin
	Rᵤ(u,p) = ∂t(u) + ∇(u)'⋅u + ∇(p) - ν*Δ(u)
	Lᵤᵃ(u,v,w) = ∇(v)'⋅u
	c₁ = 12; c₂ = 4.0
	Δxₒ = lazy_map(dx->dx^(1/2),get_cell_measure(Ω)) # This gets the characteristic element size at each element
	τᵤ(a) = 1.0 / (c₁*ν/(Δxₒ.^2) + c₂*((a⋅a).^(1/2)+1e-10)/Δxₒ ) # add (+1.0e-10) to avoid singular Jacobian (with automatic differentiation) when zero initial velocity 

	# Residual of the weak form
	res(t,(u,p),(v,q)) = 
	∫( ∂t(u)⋅v + (u⋅∇(u))⋅v + 2ν*(ε(u)⊙ε(v)) - p*(∇⋅v) + (∇⋅u)*q )dΩ +
	∫( Rᵤ(u,p) ⋅ ((τᵤ(u))*Lᵤᵃ(u,v,q)) )dΩ

	# Residual for the Stokes problem (used to initialize the solution)
	res0((u,p),(v,q)) = ∫( 2ν*(ε(u)⊙ε(v)) - p*(∇⋅v) + (∇⋅u)*q )dΩ 
	nothing # to avoid output message
end

# ╔═╡ d5182019-cba1-4faf-927c-139a1db079af
md"""
### Finite Element Operators: `FEOperator` and `TransientFEOperator`

In the finite element framework, we define the following operators to represent the variational forms of our equations:

- **Stationary Operator:** `FEOperator`

This is used to define the matrices and vectors used to solve the problem. The matrices and vectors are computed when the `solve` function is called for this operator. The Finite Element operator requires the weak form (residual) and the spaces of the trial and test functions.

- **Transient Operator:** `TransientFEOperator`

This is used to define the time-dependent matrices and vectors used to solve the problem. The matrices and vectors are computed when iterating over the solution. The Finite Element operator requires the weak form (time-dependent residual) and the spaces of the transient trial and test functions.

"""

# ╔═╡ 1570374f-19e3-4ddc-9c66-f71ba80d0646
begin
	op0 = FEOperator(res0,X(0),Y)
	op = TransientFEOperator(res,X,Y)
	nothing # to avoid output message
end

# ╔═╡ 695fd0fc-8f2a-4e91-93f8-38c228cae2f8
md"""
### Initial Condition

We solve a **steady Stokes problem** to initialize the velocity-pressure field.

- This acts as a good initial guess for transient simulation.
- We assume the time derivative is zero (`∂t(u) = 0`).
"""

# ╔═╡ e20c75e2-0613-44d7-af33-4e5a342cf880
begin
	xₕ₀ = solve(op0)
	xdotₕ₀ = interpolate_everywhere([VectorValue(0.0,0.0),0.0],X(0))
	nothing # to avoid output message
end

# ╔═╡ ee7f2671-3d84-4aee-9414-f0d71babbe02
md"""
### ⚙️ Solvers

- `NLSolver`: Newton's method with a fixed number of iterations.
- `ThetaMethod`: implicit Euler for first step (reduce high freq. oscillations in time at initialization).
- `GeneralizedAlpha1`: time integration scheme with numerical damping, suitable for transient flow.

We also define time step using the **CFL condition**. In that case, we use `CFL=1.0`
"""

# ╔═╡ f1a9aace-c747-4264-9eab-7688f07eec3c
begin
	nls = NLSolver(show_trace=true,method=:newton,iterations=10)
	h₀ = D/15
	@show Δt =  1.0*(h₀/U∞)
	ode_solver₀ = ThetaMethod(nls,Δt,1.0)
	ode_solver = GeneralizedAlpha1(nls,Δt,0.9)
end

# ╔═╡ 0e098be3-97d7-45d4-a609-e858255df605
md"""
Here we set the final time. For the first simulation, it is recommended to use only 2 steps. 

**!!! CHANGE THIS VALUE TO ~800Δt !!!**

The first run is typically slower, to have significant force characterization we want to run for large times.
"""

# ╔═╡ 12416c73-2cc2-4dbe-9ab8-88ac73d5c509
@show T = 2Δt #800Δt

# ╔═╡ 86e0aabe-f027-442b-a12a-da318e8528fc
md"""
### ▶️ Time-dependent solution

We perform:

1. A first implicit Euler step.
2. Then continue with the Generalized-α method.

At each time step, we solve the nonlinear system and update the solution.

Note that the solution is only computed when looping over the steps. For the second iteration we need to embedd the postprocessing within the time-stepping loop.
"""

# ╔═╡ cafed452-09c3-440b-a380-da0c246a642e
begin
	xₕₜ₀ = solve(ode_solver₀,op,0,Δt,xₕ₀)
	for (t,xh) in xₕₜ₀ # Iterate to get the first step only
		global xₕ₁ = xh
	end
	xₕₜ = solve(ode_solver,op,Δt,T,(xₕ₁,xdotₕ₀))
end

# ╔═╡ 2ab3905b-2be1-49fd-9d2d-67a70ed9a05d
md"""
### 📊 Postprocessing

We compute the **hydrodynamic force** on the obstacle:

$$\mathbf{F} = \int_{\Gamma_\text{wall}} (2\nu \epsilon(u) \cdot \mathbf{n} - p \mathbf{n}) \, d\Gamma$$

From this, we compute the **drag coefficient**:

$$C_d = \frac{2F_x}{\rho U_\infty^2 D}$$

- Only the x-component (`F_x`) contributes to drag.
- The results are saved in `.vtk` format for visualization.
"""

# ╔═╡ ef2295d4-1e8b-4795-acbf-ffaed52e181f
begin
	ts = Float64[]; Fxs = Float64[]; Fys = Float64[]
	filename = "data/square_$(Re)_$(U∞)"
	createpvd(filename) do pvd
		for (t,xₕ) in xₕₜ
	        uₕ,pₕ = xₕ
	        F = ∑( ∫( 2ν*ε(uₕ)⋅nΓwall - pₕ*nΓwall )dΓwall ) 
	        Cd = 2 * F / (D * U∞^2)
	        println("t = $t, F = $F", " Cd = $Cd")
			push!(ts,t)
			push!(Fxs,F[1])
			push!(Fys,F[2])
			pvd[t] = createvtk(Ω,filename*"_$t",cellfields=["u"=>xₕ[1],"p"=>xₕ[2]], order=order)
		end
	end
	nothing # to avoid output message
end

# ╔═╡ 9a7db808-a9da-4061-9c15-ee44ecdc6455
md"""
### 📉 Visualize Drag Coefficient

We plot the evolution of drag force and drag coefficient over time. This allows us to:

- Monitor convergence.
- Observe any oscillations or transient flow patterns.
- Compare with experimental or benchmark values.
"""

# ╔═╡ 4ebd6af9-e337-4832-b480-05669e70ca1d
begin
	Cds = 2 * Fxs / (D * U∞^2)
	plt = Plots.plot(Cds)
	display(plt)
	savefig(plt,"plots/square_$(Re)_$(U∞).png")
end

